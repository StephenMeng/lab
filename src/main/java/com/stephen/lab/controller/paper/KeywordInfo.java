package com.stephen.lab.controller.paper;

/**
 * @author  stephen
 */
public class KeywordInfo {
    private String word;
    private double freq;
    private int year;

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public double getFreq() {
        return freq;
    }

    public void setFreq(double freq) {
        this.freq = freq;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
